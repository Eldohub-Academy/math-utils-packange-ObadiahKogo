##AREA OF A CIRCLE
import math
def AreaCircle(radius):
    return math.pi*radius**2
radius=7
print(AreaCircle(radius))


##AREA OF A SQUARE
def AreaSquare(length):
    return length**2
length=6
print(AreaSquare(length))


##AREA OF A RECTANGLE
def AreaRectangle(length,width):
    return length*width
length=12
width=9
print(AreaRectangle(length,width))